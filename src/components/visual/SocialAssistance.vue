<script>
import { get, post } from "../../api/http";
import Utils from "../common/Utils";
import { Loading, PopupPicker } from "vux";
export default {
  components: {
    Loading,
    PopupPicker
  },
  data() {
    return {
      wRegionName: "",
      showLoading: false, //加载动画
      ThisYear: "2019", //当前年
      firsrtQuery: true,
      wData: "", //选择的时间
      wDataName: "",
      wRegion: "", //选择的地址
      wRegionName: "",
      allMoney: "", //社会帮扶捐赠资金汇总  总计
      socialAssistanceList: [], //社会帮扶捐赠资金汇总  数据
      allGoods: "", //社会帮扶捐赠物品汇总  总计
      goodsList: [], //社会帮扶捐赠物品汇总  数据
      companyAllMoney: "", //牵头单位捐赠  总计
      companyList: "", //牵头单位捐赠  数据
      StatisticsList: [
        {
          src: require("../../assets/img/Statistics-icon-22.png"),
          title: "社会扶贫协作捐款",
          value: "0万元"
        },
        {
          src: require("../../assets/img/Statistics-icon-23.png"),
          title: "社会扶贫捐物折款",
          value: "0万元"
        },
        {
          src: require("../../assets/img/Statistics-icon-24.png"),
          title: "牵头单位",
          value: "0家"
        },
        {
          src: require("../../assets/img/Statistics-icon-25.png"),
          title: "帮扶项目",
          value: "0项"
        }
      ],
      // popupPicker的组件
      showData: false,
      showAddress: false,
      dataArr: [],
      addressArr: []
    };
  },
  mounted() {
    Utils.checkLoginStatus(this, () => {
      this.init();
    });
    // this.initMobileSelect();
  },
  methods: {
    // 初始化筛选选项
    init() {
      let that = this;
      let ThisYear = new Date().getFullYear();
      that.ThisYear = ThisYear;
      Utils.showLoading(that);
      // 区域
      post(
        "/API/PoorHelpAPI/GetRegions",
        JSON.stringify({
          UserId: that.$store.state.user.UserId
        })
      )
        .then(resp => {
          if (resp.ret == "0") {
            let addressList = [];
            for (let i = 0; i < resp.data.length; i++) {
              addressList.push({
                name: resp.data[i].Name,
                value: resp.data[i].Code
              });
            }
            that.wRegion = resp.data[0].Code;
            that.wRegionName = resp.data[0].Name;
            that.addressArr.push(addressList);
            that.initData();
          } else {
            Utils.hideLoading(that);
            tils.toast(that, "获取区域数据错误" + resp.status);
          }
        })
        .catch(err => {
          Utils.hideLoading(that);
          // Utils.toast(that, "获取区域数据错误");
        });
      //年份
      post("/API/PoorHelpAPI/GetReportMinData")
        .then(resp => {
          if (resp.ret == "0") {
            let StartYear = resp.data;
            let YearLength = ThisYear - StartYear;
            let YearArr = [];
            YearArr.push({ name: ThisYear + "年", value: ThisYear });
            for (let j = 1; j <= YearLength; j++) {
              let prevYear = ThisYear - j;
              YearArr.push({ name: prevYear + "年", value: prevYear });
            }
            that.wData = ThisYear;
            that.wDataName = ThisYear + "年";
            that.dataArr.push(YearArr);
          } else {
            Utils.hideLoading(that);
            Utils.toast(that, "获取年份数据错误" + resp.umsg);
          }
        })
        .catch(err => {
          Utils.hideLoading(that);
          // Utils.toast(that, "获取年份数据错误");
        });
    },
    // 展示默认数据，及根据地址和日期筛选
    initData() {
      let that = this;
      // 暂时使用
      let QueryAddress = that.wRegion || "GSLX",
        QueryYear = that.wData || that.ThisYear;
      // console.log('地址',QueryAddress ,'日期',QueryYear )
      // 四条指标
      let params = JSON.stringify({
        reportArea: QueryAddress,
        reportYear: QueryYear
      });
      post("/API/PoorHelpAPI/GetSocietyHelpIndex", params)
        .then(resp => {
          if (resp.ret == "0") {
            // console.log(resp.data)
            that.StatisticsList[0].value = resp.data.Shfpxzjk + "万元";
            that.StatisticsList[1].value = resp.data.Shfpjwzk + "万元";
            that.StatisticsList[2].value = resp.data.Qtdw + "家";
            that.StatisticsList[3].value = resp.data.Bfxm + "项";
          } else {
            Utils.hideLoading(that);
            Utils.toast(that, "获取指标数据错误" + resp.umsg);
          }
        })
        .catch(err => {
          Utils.hideLoading(that);
          // Utils.toast(that, "获取指标数据错误");
        });
      // 社会帮扶捐赠资金汇总
      let socialAssistanceparams = JSON.stringify({
        reportArea: QueryAddress,
        reportYear: QueryYear,
        rType: "A51"
      });
      // console.log("socialAssistanceparams", socialAssistanceparams);
      post("/API/PoorHelpAPI/GetDetailStaticByIndx", socialAssistanceparams)
        .then(resp => {
          if (resp.ret == "0") {
            // console.log("社会帮扶捐赠资金汇总", resp);
            that.socialAssistanceList = resp.data[0].List;
            // 获取最大值，并以此值为参考值
            let MaxNum = resp.data[0].List[0].Value;
            for (let i = 0; i < resp.data[0].List.length; i++) {
              if (MaxNum < resp.data[0].List[i].Value) {
                MaxNum = resp.data[0].List[i].Value;
              }
            }
            for (let j = 0; j < resp.data[0].List.length; j++) {
              if (resp.data[0].List[j].Value == "0") {
                that.socialAssistanceList[j].proportion = "0";
              } else {
                that.socialAssistanceList[j].proportion =
                  (resp.data[0].List[j].Value / MaxNum) * 100;
              }
            }
            that.allMoney = resp.data[0].Total;
          } else {
            Utils.hideLoading(that);
            Utils.toast(that, "获取捐赠资金数据错误" + resp.umsg);
          }
        })
        .catch(err => {
          Utils.hideLoading(that);
          // Utils.toast(that, "获取捐赠资金数据错误");
        });
      // 社会帮扶捐赠物品汇总 数据获取
      let GoodsParams = JSON.stringify({
        reportArea: QueryAddress,
        reportYear: QueryYear,
        rType: "A451"
      });
      // console.log("GoodsParams", GoodsParams);
      post("/API/PoorHelpAPI/GetDetailStaticByIndx", GoodsParams)
        .then(resp => {
          if (resp.ret == "0") {
            // console.log("社会帮扶捐赠物品汇总", resp);
            that.allGoods = resp.data[0].Total;
            that.goodsList = resp.data[0].List;
            let timer = setTimeout(function() {
              that.goodsPic();
            }, 1500);
          } else {
            Utils.hideLoading(that);
            Utils.toast(that, "获取捐赠物品数据错误" + resp.umsg);
          }
        })
        .catch(err => {
          Utils.hideLoading(that);
          // Utils.toast(that, "获取捐赠物品数据错误");
        });
      // 牵头单位TOP10
      let LeadUnitParams = JSON.stringify({
        reportArea: QueryAddress,
        reportYear: "2019"
      });
      // console.log('LeadUnitParams',LeadUnitParams)
      post("/API/PoorHelpAPI/GetSocialDonation", LeadUnitParams)
        .then(resp => {
          if (resp.ret == "0") {
            // console.log("牵头单位TOP10", resp);
            that.companyList = resp.data[0].List;
            that.companyAllMoney = resp.data[0].Total;
            Utils.hideLoading(that);
          } else {
            Utils.hideLoading(that);
            Utils.toast(that, "获取牵头单位数据错误" + resp.umsg);
          }
        })
        .catch(err => {
          Utils.hideLoading(that);
          // Utils.toast(that, "获取牵头单位数据错误");
        });
    },
    goodsPic() {
      let DataList = this.socialAssistanceList;
      let DataTitle = [],
        DataNum = [];
      for (let i = 0; i < DataList.length; i++) {
        DataTitle[i] = DataList[i].Name;
        DataNum[i] = DataList[i].Value;
      }
      let myChart = this.$echarts.init(document.getElementById("goodsLine"));
      // 绘制图表
      myChart.setOption({
        xAxis: {
          axisLine: {
            lineStyle: {
              color: "#f1f1f1",
              width: "1"
            }
          },
          splitLine: false,
          axisTick: {
            show: false
          },
          axisLabel: {
            interval: 0,
            rotate: 45,
            textStyle: {
              color: "#888"
            }
          },
          data: DataTitle
        },
        grid: {
          left: "5%",
          right: "5%",
          top: "8%",
          bottom: "1%",
          containLabel: true
        },
        yAxis: {
          type: "value",
          splitLine: {
            lineStyle: {
              color: "#f1f1f1",
              width: "1"
            }
          },
          axisTick: {
            show: false
          },
          axisLabel: {
            formatter: "{value}万元"
          },
          splitNumber: 3,
          axisLine: {
            show: true,
            lineStyle: {
              color: "#888",
              width: "0"
            }
          }
        },
        series: [
          {
            type: "line",
            smooth: true, //柔和线条
            symbolSize: 6, //节点大小
            symbol: "circle", //节点类型
            itemStyle: {
              normal: {
                color: "#fbb444", //小球的颜色
                lineStyle: {
                  width: 2, //折线宽度
                  color: "#fbb444" //折线颜色
                }
              }
            },
            label: {
              normal: {
                show: true,
                position: "top",
                textStyle: {
                  fontSize: 14,
                  color: "#333"
                }
              }
            },
            areaStyle: {
              normal: {
                color: {
                  type: "linear", //设置线性渐变
                  x: 0,
                  y: 0,
                  x2: 0,
                  y2: 1,
                  colorStops: [
                    {
                      offset: 0,
                      color: "rgba(251,180,68,0.4)" // 0% 处的颜色
                    },
                    {
                      offset: 1,
                      color: "rgba(251,180,68,0)" // 100% 处的颜色
                    }
                  ],
                  globalCoord: false // 缺省为 false
                }
              }
            },
            data: DataNum
          }
        ]
      });
    },
    // 显示popupPick
    ShowDataPup() {
      this.showData = true;
    },
    ShowAddressPup() {
      this.showAddress = true;
    },
    // 选择改变地区
    onChangeAddress(val) {
      let that = this;
      that.wRegion = val[0];
      if (
        !!that.addressArr &&
        !!that.addressArr[0] &&
        that.addressArr[0] instanceof Array
      ) {
        for (let i = 0; i < that.addressArr[0].length; i++) {
          if (val[0] == that.addressArr[0][i].value) {
            this.wRegionName = that.addressArr[0][i].name;
          }
        }
      }
    },
    // 选择改变时间
    onChangeData(val) {
      let that = this;
      that.wData = val[0];
      that.wDataName = val[0] + "年";
    },
  },
  watch: {
    wRegion() {
      this.initData();
    },
    wData() {
      this.initData();
    }
  }
};
document.title = "社会帮扶";
</script>
